export let mapPosition = 0;
export const mapsName = Object.keys(maps);

// Charge (dessine) la map dans la grille
export function loadMap(mapKey) {
  if (!squares || !maps[mapKey]) return;

  const selectedMap = maps[mapKey];
  // On applique la map
  for (let i = 0; i < selectedMap.length; i++) {
    if (selectedMap[i] === 1 && squares[i]) {
      squares[i].classList.add("trap");
      squares[i].textContent = "üí†"; // Mur
    }
  }
}

// R√©initialise la grille (efface tout)
export function resetGrid() {
  for (let i = 0; i < 230; i++) {
    squares[i].classList.remove("block", "taken", "trap", ...letters);
    squares[i].textContent = "";
  }
  startNewTetromino();
}

// Initialisation
export function initMap() {
  console.log("Map system initialized");

  const prevMapBtn = document.getElementById("prevMap");
  const nextMapBtn = document.getElementById("nextMap");
  const mapDisplay = document.getElementById("map");

  // Affiche la premi√®re map
  if (mapDisplay) mapDisplay.textContent = mapsName[mapPosition];
  loadMap(mapsName[mapPosition]);

  // Bouton pr√©c√©dent
  if (prevMapBtn) {
    prevMapBtn.addEventListener("click", () => {
      if (mapPosition > 0) {
        resetGrid();
        mapPosition--;
        if (mapDisplay) mapDisplay.textContent = mapsName[mapPosition];
        loadMap(mapsName[mapPosition]);
        console.log("mapPosition", mapsName[mapPosition]);
      }
    });
  }

  // Bouton suivant
  if (nextMapBtn) {
    nextMapBtn.addEventListener("click", () => {
      if (mapPosition < mapsName.length - 1) {
        resetGrid();
        mapPosition++;
        if (mapDisplay) mapDisplay.textContent = mapsName[mapPosition];
        loadMap(mapsName[mapPosition]);
      }
    });
  }
}
